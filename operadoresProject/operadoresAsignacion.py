# Ejemplos de operadores de asignacion
var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

var1 += 3 #var1 = var1 +3 asignacion con suma
var2 -= 3 #var2 = var2-3 asignacion con resta
var3 *= 3 #var3 = var3*3 asignacion con multiplicacion
var4 /= 3 #var4 = var4/3 asignacion con division
var5 %= 3 #var5 = var5%3 asignacion del residuo
var6 //= 3 #var6 = var6//3 asignacion con division entera
print(f"asignacion con suma: {var1}")
print(f"asignacion con resta: {var2}")
print(f"asignacion con multiplicacion: {var3}")
print(f"asignacion con division: {var4}")
print(f"asignacion con modulo: {var5}")
print(f"asignacion con division entera: {var6}")
